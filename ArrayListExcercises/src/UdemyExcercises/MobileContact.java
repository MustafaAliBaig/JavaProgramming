package UdemyExcercises;

public class MobileContact {
	
	private String name;
	private int phno;
	
	public MobileContact(String name, int phno) {
		this.name = name;
		this.phno = phno;
	}

	public String getName() {
		return name;
	}

	public int getPhno() {
		return phno;
	}
	
}
